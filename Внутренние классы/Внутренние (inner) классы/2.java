/**
 * Практика #2
 * Имеет ли внутренний класс доступ к полям и методам внешнего класса? Как возможность доступа зависит от спецификаторов доступа? Приведите примеры кода.
 *
 * @author Aleksandra
 *
 */

class OuterClass {
    private String outerField = "Приватное поле"; // Приватное поле внешнего класса

    public class InnerClass {
        // Метод внутреннего класса для доступа к полю внешнего класса
        void displayOuter() {
            // Выводим значение приватного поля внешнего класса
            System.out.println("Доступ к полю внешнего класса из InnerClass: " + outerField);
        }
    }

    // Метод для создания внутреннего класса и вызова его метода
    public void createInner() {
        InnerClass inner = new InnerClass(); // Создаем экземпляр внутреннего класса
        inner.displayOuter(); // Вызов метода внутреннего класса
    }
}

public class Main {
    public static void main(String[] args) {
        OuterClass outer = new OuterClass(); // Создаем экземпляр внешнего класса
        outer.createInner(); // Создаем внутренний класс и выводим данные
    }
}

/*
Пояснение:

Внутренний класс InnerClass может получить доступ к полям и методам внешнего класса OuterClass, включая те, которые имеют спецификатор доступа private.
В методе displayOuter() внутреннего класса мы обращаемся к приватному полю outerField внешнего класса.
Метод createInner() в внешнем классе используется для создания экземпляра внутреннего класса и вызова его метода, что демонстрирует доступ к полям внешнего класса.
*/