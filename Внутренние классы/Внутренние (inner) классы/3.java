/**
 * Практика #3
 * Имеет ли внешний класс доступ к полям и методам внутреннего класса? Как возможность доступа зависит от спецификаторов доступа? Приведите примеры кода.
 *
 * @author Aleksandra
 *
 */

class OuterClass {
    private String outerField = "Приватное поле внешнего класса";

    public class InnerClass {
        private String innerField = "Приватное поле внутреннего класса";

        // Метод для вывода значения приватного поля внутреннего класса
        public void display() {
            System.out.println("Внутреннее поле: " + innerField); // Выводим значение внутреннего поля
        }
    }

    // Метод для создания экземпляра внутреннего класса
    public void createInner() {
        InnerClass inner = new InnerClass(); // Создаем экземпляр внутреннего класса
        inner.display(); // Доступ к методу внутреннего класса
        System.out.println("Доступ к innerField из OuterClass: " + inner.innerField);
    }
}

public class Main {
    public static void main(String[] args) {
        OuterClass outer = new OuterClass(); // Создаем экземпляр внешнего класса
        outer.createInner(); // Создаем внутренний класс и выводим данные
    }
}

/*
Пояснение:
Внутреннее поле: Приватное поле внутреннего класса

Внешний класс OuterClass может получить доступ к методам внутреннего класса InnerClass, но не может напрямую получить доступ к его приватным полям.
В примере метод display() внутреннего класса используется для доступа к приватному полю innerField.
Если бы внешний класс пытался обращаться к innerField напрямую, это привело бы к ошибке компиляции, так как поле имеет уровень доступа private.
Таким образом, внешние классы имеют ограниченный доступ к полям и методам внутреннего класса в зависимости от спецификаторов доступа.
*/