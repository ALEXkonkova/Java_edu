public class Main {
    public static void main(String[] args) {
        int a = 2; // значение a
        int k = 0;

        // Использование тернарного оператора для определения значения i
        int i = (a == 1) ? 1 : (a == 2) ? 2 : 3;

        // Использование условного оператора для определения значения k (из задания)
        if (a == 1) {
            k = 1;
        } else if (a == 2) {
            k = 2;
        } else {
            k = 3;
        }

        // Выводим результат и проверяем что значения совпадают
        System.out.println("Значение i: " + i);
        System.out.println("Значение k: " + k);
    }
}